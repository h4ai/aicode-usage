import os
import pytest
import requests

BASE_URL = os.getenv("AI_CODE_USAGE_BACKEND", "http://localhost:8002")


def _login(username: str, password: str) -> requests.Session:
    s = requests.Session()
    # NOTE: current backend exposes /api/auth/test-login for test envs.
    r = s.post(f"{BASE_URL}/api/auth/test-login", json={"username": username, "password": password}, timeout=20)
    assert r.status_code == 200, r.text
    data = r.json()
    token = data.get("token")
    assert token, data
    s.headers.update({"Authorization": f"Bearer {token}"})
    return s


